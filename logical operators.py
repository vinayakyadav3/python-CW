'''
logical operators : help us combining result of two conditions
and = returns true if both statement are true
or = returns true if one of statement is true
not = reverse the result, returns false if result is true
'''

expression1 = 2>1 #True
expression2 = 5<4 #False
print("exp1 and exp 2:", expression1 and expression2)
print("exp1 or exp 2:", expression1 or expression2)
print("not exp1:", not expression1)

