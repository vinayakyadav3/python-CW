'''
+   Addition
-   Substraction
*   multiplication
/   division.   ex. 4/3 = 1.333
%   Modulus = it returns reminder.  ex. 5%2 = 1
**  exponentiation ex. n1**n2 means n1 ^ n2 (raise to power)
//  floor division = division result in whole number.   ex. 4/3 = 1
'''

print("sum:", 4+3)
print("difference:", 4-3)
print("product:", 4*3)
print("division:", 4/3)
print("floor division:", 4//3)
print("remainder:", 4%3)
print("exponentiation:", 4**3)

